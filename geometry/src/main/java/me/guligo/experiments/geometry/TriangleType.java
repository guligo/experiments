package me.guligo.experiments.geometry;

/**
 * Represents triangle types:
 * 
 * <ul>
 *     <li>Scalene - A triangle with no identical sides;</li>
 *     <li>Isosceles - A triangle with *at least* two identical sides;</li>
 *     <li>Equilateral - A triangle with three identical sides.</li>
 * </ul>
 * 
 * @author guligo
 */
public enum TriangleType {

	SCALENE,
	ISOSCELES,
	EQUILATERAL;

}
